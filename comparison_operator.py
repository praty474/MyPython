#used to compare two values
a = 5
b = 6

print(f'{a} equals {b} ',a==b) #equals to

print(f'{a} not equals to {b}',a!=b) #not equals to

print(f'{a} greater than {b}',a > b) #greater than

print(f'{a} less than {b}', a < b) #less than

print(f'{a} less than or equals to {b}',a <= b) #less than or equals to

print(f'{a} greater than or equals to {b}',a >= b) #greater than or equals to

